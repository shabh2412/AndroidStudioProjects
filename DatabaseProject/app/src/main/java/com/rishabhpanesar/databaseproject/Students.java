package com.rishabhpanesar.databaseproject;

public class Students {
    public String Stu_Name;
    public String Stu_Roll;
    public String Stu_Department;
    public String Stu_Marks;

    public Students(String stu_Name, String stu_Roll, String stu_Department, String stu_Marks) {
        Stu_Name = stu_Name;
        Stu_Roll = stu_Roll;
        Stu_Department = stu_Department;
        Stu_Marks = stu_Marks;
    }
}
